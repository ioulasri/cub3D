# include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*str;
	size_t	j;

	if (!s)
		return (NULL);
	while (start-- && *s)
		s++;
	if (len > ft_strlen(s))
		len = ft_strlen(s);
	str = malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	if (!*s)
	{
		str[0] = '\0';
		return (str);
	}
	i = 0;
	j = 0;
	while (s[i] && i < len && i < ft_strlen(s))
		str[j++] = s[i++];
	str[j] = '\0';
	return (str);
}
